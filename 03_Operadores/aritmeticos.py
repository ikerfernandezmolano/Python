# Operadores aritméticos
num1 = 33
num2 = 15

# Suma
suma = num1 + num2
print(f"El resultado es {suma}")

# Resta
resta = num1 - num2
print(f"El resultado es {resta}")

# Multiplicación
mult = num1 * num2
print(f"El resultado es {mult}")

# División
div = num1 / num2
print(f"El resultado es {div}")

# Resto
resto = num1 % num2
print(f"El resultado es {resto}")

# Potencia
pot = num1 ** num2
print(f"El resultado es {pot}")